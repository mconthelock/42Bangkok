#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c[10];
	int		n;
	int		i;

	i = 0;
	while (nb > 0)
	{
		n = nb % 10;
		c[i] = n + 48;
		nb = nb / 10;
		++i;
	}
	--i;
	while (i >= 0)
	{
		write(1, &c[i], 1);
		--i;
	}
}
