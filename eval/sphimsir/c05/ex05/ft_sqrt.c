#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 1)
		return (nb);
	while (i <= nb / i)
	{
		if (i * i == nb)
			return (i);
		i ++;
	}
	return (0);
}
